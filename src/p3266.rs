const MODULO: i64 = 1_000_000_000 + 7;

impl Solution {
    pub fn get_final_state(nums: Vec<i32>, k: i32, multiplier: i32) -> Vec<i32> {
        let mut nums: Vec<ModuloI64> = nums
            .into_iter()
            .map(|val| ModuloI64::new(val as i64))
            .collect();

        let k = k as u32;
        let multiplier: ModuloI64 = (multiplier as i64).into();

        let mut ranked_nums: Vec<_> = nums
            .iter()
            .enumerate()
            .map(|(idx, &val)| LogNum::new(idx, *val, *multiplier))
            .collect();
        ranked_nums.sort_unstable();

        let rank_counts = count_ranks(&ranked_nums);

        // Locate the place of rank jump
        let idx1 = match rank_counts.binary_search_by_key(&k, |entry| entry.n_muls) {
            Ok(idx) => idx,
            Err(idx) => idx - 1,
        };

        // Multiply numbers which ranks are lower than `rank1`
        let RankCount {
            rank: rank1,
            n_lt_rank: n_lt_rank1,
            n_muls: n_muls_to_rank1,
        } = rank_counts[idx1];

        {
            let mut prev_rank = rank1;
            let mut m: ModuloI64 = 1.into();

            for entry in ranked_nums[0..n_lt_rank1].iter_mut().rev() {
                let rank_diff = prev_rank - entry.rank;
                if rank_diff > 0 {
                    m *= multiplier.pow(rank_diff);
                }

                prev_rank = entry.rank;
                entry.rank = rank1;
                nums[entry.idx] *= m;
            }
        }

        // Sort numbers which ranks were lifted to `rank1`
        let k2 = k - n_muls_to_rank1;
        let idx4 = idx1 + 1;
        let rank_count4 = rank_counts.get(idx4);
        let n_le_rank1 = match rank_count4 {
            Some(entry) => entry.n_lt_rank,
            None => ranked_nums.len(),
        };
        ranked_nums[0..n_le_rank1].sort_unstable();

        // Multiply numbers to `rank2`

        let k3 = {
            let rank_diff = k2 / n_le_rank1 as u32;
            let rank2 = rank1 + rank_diff;
            let m = multiplier.pow(rank_diff);

            for entry in &mut ranked_nums[0..n_le_rank1] {
                entry.rank = rank2;
                nums[entry.idx] *= m;
            }

            k2 - n_le_rank1 as u32 * rank_diff
        };
        let n_le_rank2 = n_le_rank1;

        // Multiply first `k3` numbers to `rank3`
        debug_assert!(k3 < n_le_rank2 as u32);
        let k3 = k3 as usize;

        ranked_nums.sort_unstable();

        for entry in &mut ranked_nums[0..k3] {
            entry.rank += 1;
            nums[entry.idx] *= multiplier;
        }

        nums.into_iter().map(|val| *val as i32).collect()
    }
}

#[derive(Debug, Clone, Copy)]
struct RankCount {
    rank: u32,
    n_lt_rank: usize,
    n_muls: u32,
}

fn count_ranks(ranked_nums: &[LogNum]) -> Vec<RankCount> {
    let mut iter = ranked_nums.iter();

    let mut rank_counts = Vec::with_capacity(ranked_nums.len());

    let first = iter.next().unwrap();
    let mut prev_rank = first.rank;
    let mut n_lt_rank: usize = 1;
    let mut n_muls = 0;

    rank_counts.push(RankCount {
        rank: first.rank,
        n_lt_rank: 0,
        n_muls: 0,
    });

    for entry in iter {
        let diff_rank = entry.rank - prev_rank;
        if diff_rank > 0 {
            n_muls += diff_rank * n_lt_rank as u32;
            rank_counts.push(RankCount {
                rank: entry.rank,
                n_lt_rank,
                n_muls,
            });
            prev_rank = entry.rank;
        }
        n_lt_rank += 1;
    }

    rank_counts
}

#[derive(Debug, Clone, Copy)]
struct LogNum {
    idx: usize,
    fract: f64,
    rank: u32,
}

impl LogNum {
    pub fn new(idx: usize, num: i64, m: i64) -> Self {
        let log = (num as f64).log(m as f64);
        let mut rank = log.floor() as u32;
        let mut fract = num as f64 / m.pow(rank) as f64;

        // Fix the precision error when the log is N.99999999....
        if fract >= m as f64 {
            rank += 1;
            fract /= m as f64;
        }

        Self { idx, fract, rank }
    }
}

impl PartialEq for LogNum {
    fn eq(&self, other: &Self) -> bool {
        self.fract == other.fract && self.rank == other.rank
    }
}

impl Eq for LogNum {}

impl PartialOrd for LogNum {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        (self.rank, self.fract, self.idx).partial_cmp(&(other.rank, other.fract, other.idx))
    }
}

impl Ord for LogNum {
    fn cmp(&self, other: &Self) -> Ordering {
        self.partial_cmp(other).unwrap()
    }
}

#[derive(Clone, Copy, PartialEq, Eq, Hash)]
#[repr(transparent)]
struct ModuloI64(i64);

impl ModuloI64 {
    pub fn new(val: i64) -> Self {
        Self(val % MODULO)
    }

    pub fn pow(&self, power: u32) -> Self {
        if power == 0 {
            return Self::new(1);
        }

        let base = *self;
        let mut output = *self;
        let mut bit = 1u32 << (u32::BITS - power.leading_zeros() - 1);

        loop {
            bit >>= 1;
            if bit == 0 {
                break;
            }
            output *= output;

            if power & bit != 0 {
                output *= base;
            }
        }

        output
    }
}

impl Deref for ModuloI64 {
    type Target = i64;

    fn deref(&self) -> &Self::Target {
        &self.0
    }
}

impl From<i64> for ModuloI64 {
    fn from(value: i64) -> Self {
        Self(value)
    }
}

impl Debug for ModuloI64 {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        Display::fmt(&self.0, f)
    }
}

impl Display for ModuloI64 {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        Display::fmt(&self.0, f)
    }
}

impl Add for ModuloI64 {
    type Output = Self;

    fn add(self, rhs: Self) -> Self::Output {
        Self((self.0 + rhs.0) % MODULO)
    }
}

impl Sub for ModuloI64 {
    type Output = Self;

    fn sub(self, rhs: Self) -> Self::Output {
        Self((self.0 + MODULO - rhs.0) % MODULO)
    }
}

impl Mul for ModuloI64 {
    type Output = Self;

    fn mul(self, rhs: Self) -> Self::Output {
        Self((self.0 * rhs.0) % MODULO)
    }
}

impl AddAssign for ModuloI64 {
    fn add_assign(&mut self, rhs: Self) {
        *self = *self + rhs;
    }
}

impl MulAssign for ModuloI64 {
    fn mul_assign(&mut self, rhs: Self) {
        *self = *self * rhs;
    }
}

